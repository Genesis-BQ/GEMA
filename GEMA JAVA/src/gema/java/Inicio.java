    private void btnConsultarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnConsultarActionPerformed
         // Establecer una conexi칩n con el servidor
    
        String serverAddress = "127.0.0.1";
        int serverPort = 8888;

        try {
            Socket socket = new Socket(serverAddress, serverPort);

            OutputStream outputStream = socket.getOutputStream();
            PrintWriter writer = new PrintWriter(outputStream, true);

            String solicitud = "TODO ";
            writer.println(solicitud);

            InputStream inputStream = socket.getInputStream();
            BufferedReader reader = new BufferedReader(new InputStreamReader(inputStream));

            StringBuilder respuesta = new StringBuilder();
            String linea;
            while ((linea = reader.readLine()) != null) {
                respuesta.append(linea).append("\n");
            }

            if (respuesta != null) {
                if (!respuesta.toString().trim().equals("ERROR")) {
                    String[] registros = respuesta.toString().split("\n");

                    DefaultTableModel model = (DefaultTableModel) Tabla.getModel();
                    model.setRowCount(0);

                    for (String registro : registros) {
                        String[] columns = registro.split(",");
                        if (columns.length >= 8) {
                            model.addRow(new Object[]{columns[0], columns[1], columns[2], columns[3], columns[4], columns[5], columns[6], columns[7]});
                        }
                    }
                } else {
                    JOptionPane.showMessageDialog(this, "Error al consultar los registros.");
                }
            }

            socket.close();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Error de conexi칩n: " + ex.getMessage());
        }
    }//GEN-LAST:event_btnConsultarActionPerformed

